package edu;

public class Test27 {

	public static void main(String[] args) {
		//for문
		for(int i=8; i<10; i++) {
			System.out.println("for ok");
		}
		
		//while문
		int j=8;
		while(j<10) {
			System.out.println("while ok");
			j++;
		}
		
		//do-while문
		int k=7;
		do {
			System.out.println("do-while ok");
			k++;
		}while(k<10);

	}

}
